// Dato del tipo number

let edad = 33;
console.log(edad);

// Dato del tipo number con decimales

let precio = 234.78;
console.log(precio);

// Dato del tipo string con comillas dobles

let nombre = "manuel";
console.log(nombre);

// Dato del tipo string con comillas simples

let apellido = 'guerra';
console.log(apellido);

// Dato booleano con valor true 

let verdadero = true;
console.log(verdadero);

// Dato booleano con valor false

let falso = false;
console.log(falso);

// Dato del tipo NaN

let NotANumber = NaN;
console.log(NotANumber);

// Dato del tipo Null

let nulo = null;
console.log (nulo);

// Dato undefined

let SinValor = "Sin Valor";
console.log(SinValor);

// Dato del tipo objeto literal

let persona = {
nombre: 'Lucas',
edad: 57
}
console.log(persona);

// Dato del tipo array

let paises = [ 'Argentina', 'Venezuela', 'Colombia'];
console.log(paises);