// Suma 

console.log( 5 + 4 ); 

// Resta

console.log( 10 - 6);

// Multiplicacion

console.log( 3 * 4 );

// Division

console.log( 10 / 2 );

// Modulo 

console.log( 10 % 2 );

// Incremento
let numero = 6;
