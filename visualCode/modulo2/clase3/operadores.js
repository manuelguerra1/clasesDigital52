// Comparacion simple
console.log( 5 == '5');

// Comparacion estricta
console.log( 5 === '5');

// Desigualdad simple
console.log( 5 != '5');

// Desigualdad estricta
console.log( 5 !== '5');

// Mayor que
console.log( 10 > 6 );
console.log(10 > 10 );

// Mayor o igual que
console.log( 6 >= 6 );

// Menor
console.log( 7 < 4 );
console.log( 7 < 7 );

// Menor o igual que
console.log( 6 <= 6 );

// And &&
console.log( true && true );
